		$.each(numbers, function(index)
		{
			var number;

			number = numbers[index] * 1;
			// convert to hex
			number = number.toString(16);
			// enforce double-digit
			if(number.length < 2)
			{
				number = "0" + number;
			}

			result += number;
		});

		return result;
